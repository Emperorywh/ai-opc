/**
 * 南极洲特判：南纬高纬带 + 南极半岛向北凸。
 * @param {number} lon
 * @param {number} lat
 */
function isAntarctica(lon, lat) {
  if (lat < -63) return true
  // 南极半岛（~-60° 经度）向北延伸到约 -56°
  if (lat < -60 && lon > -75 && lon < -55) return true
  return false
}

/**
 * 射线法判断点是否在多边形内（含边界近似）。
 * @param {number} lon
 * @param {number} lat
 * @param {LonLat[]} ring
 */
function pointInRing(lon, lat, ring) {
  let inside = false
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i][0]
    const yi = ring[i][1]
    const xj = ring[j][0]
    const yj = ring[j][1]
    const intersect = yi > lat !== yj > lat && lon < ((xj - xi) * (lat - yi)) / (yj - yi) + xi
    if (intersect) inside = !inside
  }
  return inside
}

/**
 * 该点是否为陆地（任一大陆/岛屿多边形内 或 南极洲）。
 * @param {number} lon  [-180,180]
 * @param {number} lat  [-90,90]
 * @returns {boolean}
 */
export function isLand(lon, lat) {
  if (isAntarctica(lon, lat)) return true
  for (const { ring } of LAND_RINGS) {
    if (pointInRing(lon, lat, ring)) return true
  }
  return false
}
